    private int searchCoach(String chosenSport) {
        File f = new File("coach_details.txt");
        
        try {
            Scanner buffer = new Scanner(f);
            
            while (buffer.hasNext()) {
                String line = buffer.nextLine();
                if (line.contains("SPORT:")) {
                    if (line.substring(7).equals(chosenSport)) {
                        for (int i = 0; i <= 1; i++) {
                            line = buffer.nextLine();
                        }                     
                        return Integer.parseInt(line.substring(8));
                    }
                }
            }
        } catch (Exception e) {
            System.out.println("File does not exist.");
        }
        return 0;
    }
    
    private boolean saveRecord(String chosenSport, int rating) {
        File f = new File("coach_details.txt");
        String content = "";
        String newContent = "";
        try {
            BufferedReader br = new BufferedReader(new FileReader(f));
            String line = br.readLine();
            
            while (line != null) {
                content += line + "\n";
                line = br.readLine();
            }
            br.close();
            
            Scanner s = new Scanner(content);
            
            while (s.hasNext()) {
                String line2 = s.nextLine();
                if (line2.contains("SPORT:")) {
                    if (line2.substring(7).equals(chosenSport)) {
                        newContent += line2 + "\n";
                        line2 = s.nextLine();
                        newContent += line2 + "\n";
                        line2 = s.nextLine();
                        newContent += line2.substring(0, 8) + rating + "\n";
                    } else {
                        newContent += line2 + "\n";
                    }
                } else {
                    newContent += line2 + "\n";
                }
            }
            
            FileWriter fw = new FileWriter(f);
            fw.write(newContent + "\n");
            fw.close();
            
            return true;
        } catch (Exception e) {
            System.out.println("File does not exist.");
        }
        return false;
    }
