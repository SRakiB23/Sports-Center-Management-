    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        String chosenSport = (String)ChosenSportInput.getSelectedItem();
        int rating = RatingInput.getValue();
        
        int oldRating = searchCoach(chosenSport);
        
        rating += oldRating;
        boolean isSuccess = saveRecord(chosenSport, rating);
        
        if (isSuccess) {
            JOptionPane.showMessageDialog(this, "Feedback saved successfully.", "Successful", JOptionPane.PLAIN_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Unable to save feedback.", "Error", JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_SubmitButtonActionPerformed

